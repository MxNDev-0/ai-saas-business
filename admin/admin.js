/* =========================================
   POSTS SYSTEM
========================================= */

let allPosts = [];

/* LOAD POSTS */
function loadPosts() {

  const box = document.getElementById("postsList");

  const q = query(collection(db, "posts"), orderBy("createdAt", "desc"));

  onSnapshot(q, (snap) => {

    allPosts = [];
    snap.forEach(d => allPosts.push({ id: d.id, ...d.data() }));

    renderPosts(allPosts);
  });
}

expose("loadPosts", loadPosts);

/* =========================================
   FIXED EDIT SYSTEM (INJECTED)
========================================= */

window.currentEditPostId = null;

window.loadIntoEditor = function (post) {

  window.currentEditPostId = post.id;

  document.getElementById("editPostId").value = post.id || "";
  document.getElementById("editPostTitle").value = post.title || "";
  document.getElementById("editPostContent").value = post.content || "";

  log("✏ Post loaded into editor");
};

window.updatePost = async function () {

  const id = window.currentEditPostId;

  const title = document.getElementById("editPostTitle")?.value;
  const content = document.getElementById("editPostContent")?.value;

  if (!id) return log("No post selected", "warn");

  try {

    await updateDoc(doc(db, "posts", id), {
      title,
      content,
      updatedAt: serverTimestamp()
    });

    log("✏ Post updated");

  } catch (err) {
    console.error(err);
    log("Update failed", "error");
  }
};

/* =========================================
   RENDER POSTS (UPDATED WITH EDIT)
========================================= */

function renderPosts(posts) {

  const box = document.getElementById("postsList");
  box.innerHTML = "";

  posts.forEach(p => {

    const safe = JSON.stringify(p).replace(/"/g, '&quot;');

    box.innerHTML += `
      <div class="item">

        <b>${p.title}</b><br>

        <small>${p.aiGenerated ? "🤖 AI POST" : "✍ Manual"}</small><br><br>

        <button onclick='loadIntoEditor(${safe})'>
          Edit
        </button>

        <button onclick="deletePost('${p.id}')">
          Delete
        </button>

      </div>
    `;
  });
}

/* DELETE */
window.deletePost = async (id) => {
  await deleteDoc(doc(db, "posts", id));
  log("🗑 Deleted post");
};

/* =========================================
   ADS SYSTEM
========================================= */

function loadAds() {

  const box = document.getElementById("upgradeList");

  onSnapshot(collection(db, "adRequests"), (snap) => {

    box.innerHTML = "";

    snap.forEach(d => {

      box.innerHTML += `
        <div class="item">
          <b>${d.data().title}</b><br>
          <button onclick="acceptAd('${d.id}')">Accept</button>
          <button onclick="rejectAd('${d.id}')">Reject</button>
        </div>
      `;
    });

  });
}

expose("loadAds", loadAds);

window.acceptAd = async (id) => {
  await updateDoc(doc(db, "adRequests", id), { status: "accepted" });
  log("✅ Ad accepted");
};

window.rejectAd = async (id) => {
  await updateDoc(doc(db, "adRequests", id), { status: "rejected" });
  log("❌ Ad rejected");
};