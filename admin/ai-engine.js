/* =========================================
   5. MONETIZATION LAYER (AUTO ADS / AFFILIATES)
========================================= */

function injectMonetization(content) {

  return content +
`

---

💰 Sponsored Links:
- Best hosting for creators → example.com
- AI tools for bloggers → example.com

`;
}

/* =========================================
   6. FULL AUTO BLOG GENERATOR
========================================= */

export async function autoGenerateBlog(topic) {

  const content = await generateAIArticle(topic);

  const seo = generateSEO(topic, content);
  const image = await generateImage(topic);

  const finalContent = injectMonetization(content);

  const post = {
    title: topic,
    content: finalContent,
    image,
    seo,
    aiGenerated: true,
    createdAt: serverTimestamp(),
    status: "auto-published"
  };

  await addDoc(collection(db, "posts"), post);

  return post;
}

/* =========================================
   7. AUTO SCHEDULER (OPTIONAL LOOP HOOK)
========================================= */

export async function scheduleAutoPost(topic, delayMs = 60000) {

  setTimeout(async () => {

    await autoGenerateBlog(topic);

    console.log("🚀 Auto post published:", topic);

  }, delayMs);
}

