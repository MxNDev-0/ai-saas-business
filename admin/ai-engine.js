/* =========================================
   2. SEO OPTIMIZER
========================================= */

function generateSEO(title, content) {

  const keywords = title
    .toLowerCase()
    .split(" ")
    .filter(w => w.length > 3);

  return {
    keywords: keywords.join(", "),
    description: content.slice(0, 150),
    slug: title.toLowerCase().replace(/\s+/g, "-")
  };
}

/* =========================================
   3. AI CONTENT ENGINE (CORE)
========================================= */

export async function generateAIArticle(topic) {

  if (AI_CONFIG.provider === "mock") {

    return `
# ${topic}

## Introduction
This article was generated by MCN Level 2 AI Engine.

## Main Insights
- ${topic} is trending globally
- AI automation is increasing
- Monetization opportunities are growing

## Conclusion
MCN Engine successfully generated structured content.
    `.trim();
  }

  /* OPENAI READY HOOK */
  if (AI_CONFIG.provider === "openai") {

    const res = await fetch("https://api.openai.com/v1/chat/completions", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "Authorization": `Bearer ${AI_CONFIG.apiKey}`
      },
      body: JSON.stringify({
        model: "gpt-4o-mini",
        messages: [
          {
            role: "system",
            content: "You are a professional SEO blog writer."
          },
          {
            role: "user",
            content: `Write a detailed SEO blog about: ${topic}`
          }
        ]
      })
    });

    const data = await res.json();
    return data.choices[0].message.content;
  }

  return "AI provider not configured";
}

